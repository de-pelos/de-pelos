export function formatCOP(value: number | null) {
  if (value === null) return "Consultar precio";

  return `$${new Intl.NumberFormat("es-CO").format(value)} COP`;
}

export function getProductById(id: string) {
  return products.find((product) => product.id === id);
}

export function buildProductWhatsAppMessage(product: Product) {
  return `Hola, estoy interesado en el producto: ${product.nombre} ${product.presentacion}. ¿Me puedes confirmar disponibilidad, precio final y forma de entrega?`;
}

export function buildWhatsAppHref(message = GENERAL_WHATSAPP_MESSAGE) {
  return `${WHATSAPP_URL}?text=${encodeURIComponent(message)}`;
}
